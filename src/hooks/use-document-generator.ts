function generateCollaborationMap(team: AgentTeam): string {
	let content = `╔══════════════════════════════════════════════════╗
║  COLLABORATION MAP                               ║
║  Inter-Agent Interaction Protocols               ║
║  Version: 1.0                                    ║
╚══════════════════════════════════════════════════╝

TOTAL INTERACTIONS: ${team.connections.length}

`;

	team.connections.forEach((conn) => {
		const fromAgent = team.agents.find((a) => a.id === conn.from);
		const toAgent = team.agents.find((a) => a.id === conn.to);
		if (!fromAgent || !toAgent) return;

		content += `──────────────────────────────────────────────────
INTERACTION: ${conn.interactionId}
──────────────────────────────────────────────────

AGENTS INVOLVED:
  ${fromAgent.designation} (${fromAgent.name})
  → ${toAgent.designation} (${toAgent.name})

INTERACTION TYPE: ${conn.label}

TRIGGER CONDITION:
[To be defined based on specific task dependencies]

PAYLOAD FORMAT:
{
  "interaction_id": "${conn.interactionId}",
  "from": "${fromAgent.designation}",
  "to": "${toAgent.designation}",
  "type": "${conn.label.toUpperCase().replace(/\s/g, '_')}",
  "data": { /* specific payload */ },
  "timestamp": "ISO-8601"
}

CONFIRMATION FORMAT:
{
  "interaction_id": "${conn.interactionId}",
  "status": "RECEIVED" | "PROCESSING" | "COMPLETE" | "FAILED",
  "response_data": { /* if applicable */ },
  "timestamp": "ISO-8601"
}

`;
	});

	content += `──────────────────────────────────────────────────
END OF COLLABORATION MAP
──────────────────────────────────────────────────
`;

	return content;
}

