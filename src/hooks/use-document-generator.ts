export function useDocumentGenerator() {
	const [documents, setDocuments] = useState<GeneratedDocuments>({
		masterContext: null,
		agentPRDs: [],
		collaborationMap: null,
		setupGuide: null,
	});
	const [isGenerating, setIsGenerating] = useState(false);

	const generateDocuments = useCallback((answers: InterviewAnswers, team: AgentTeam) => {
		setIsGenerating(true);

		// Simulate generation with streaming effect
		setTimeout(() => {
			const masterContext: Document = {
				id: 'master-context',
				type: 'master',
				title: 'Master Project Context',
				subtitle: 'Shared knowledge base for all agents',
				content: generateMasterContext(answers, team),
				generatedAt: new Date(),
			};

			const agentPRDs: Document[] = team.agents.map((agent) => ({
				id: `prd-${agent.id}`,
				type: 'agent' as const,
				title: `Agent PRD: ${agent.name}`,
				subtitle: agent.designation,
				agentId: agent.id,
				content: generateAgentPRD(agent),
				generatedAt: new Date(),
			}));

			const collaborationMap: Document = {
				id: 'collaboration-map',
				type: 'collaboration',
				title: 'Collaboration Map',
				subtitle: 'Inter-agent interaction protocols',
				content: generateCollaborationMap(team),
				generatedAt: new Date(),
			};

			const setupGuide: Document = {
				id: 'setup-guide',
				type: 'setup',
				title: 'User Setup & Execution Guide',
				subtitle: 'Step-by-step implementation instructions',
				content: generateSetupGuide(team),
				generatedAt: new Date(),
			};

			setDocuments({
				masterContext,
				agentPRDs,
				collaborationMap,
				setupGuide,
			});
			setIsGenerating(false);
		}, 3000);
	}, []);

	return {
		documents,
		isGenerating,
		generateDocuments,
	};
}
